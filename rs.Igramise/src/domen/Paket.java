package domen;

public class Paket {
	
	private int idPaketa, idIgraonica;
	private String nazivPaketa;
	private String opisPaketa;
	/**
	 * @return the idPaketa
	 */
	public int getIdPaketa() {
		return idPaketa;
	}
	/**
	 * @param idPaketa the idPaketa to set
	 */
	public void setIdPaketa(int idPaketa) {
		this.idPaketa = idPaketa;
	}
	/**
	 * @return the idIgraonica
	 */
	public int getIdIgraonica() {
		return idIgraonica;
	}
	/**
	 * @param idIgraonica the idIgraonica to set
	 */
	public void setIdIgraonica(int idIgraonica) {
		this.idIgraonica = idIgraonica;
	}
	/**
	 * @return the nazivPaketa
	 */
	public String getNazivPaketa() {
		return nazivPaketa;
	}
	/**
	 * @param nazivPaketa the nazivPaketa to set
	 */
	public void setNazivPaketa(String nazivPaketa) {
		this.nazivPaketa = nazivPaketa;
	}
	/**
	 * @return the opisPaketa
	 */
	public String getOpisPaketa() {
		return opisPaketa;
	}
	/**
	 * @param opisPaketa the opisPaketa to set
	 */
	public void setOpisPaketa(String opisPaketa) {
		this.opisPaketa = opisPaketa;
	}

	
}